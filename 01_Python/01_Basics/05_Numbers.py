import math

x = 1 # integer
y = 1.2 # float
z = 1 + 2j # complex

# Operations
print(5 + 3)
print(5 - 3)
print(5 * 3)
print(5 / 3) # 1.666666666667

print(10 // 3) # 3
print(10 % 3) # 1
print(10 ** 3) # 10^3 = 1000


print(math.sqrt(25)) # 5.0
print(math.floor(5.9)) # abrunden 5
print(math.ceil(5.1)) # aufrunden 6

# Increment / Decrement
x = 10
print(x) # 10

x = x - 5
print(x) # 13

x = x - 5
print(x) # 8

x = x * 5
print(x) # 40

# Shortcut
x = x + 1
x += 1  # x = x + 1
x -= 5  # x = x - 5
x *= 3  # x = x * 3
x /= 3  # x = x / 3
